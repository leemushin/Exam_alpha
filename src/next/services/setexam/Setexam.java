package next.services.setexam;

public class Setexam {
	private String EB_ID; 		//考卷編號
	private String EB_NO;		//考卷代號
	private String EB_NAME;		//考卷
	private String EB_MAIN;		//考卷內容
	
	public String getEB_ID() {
		return EB_ID;
	}
	public void setEB_ID(String EB_ID) {
		this.EB_ID = EB_ID;
	}	
	public String getEB_NO() {
		return EB_NO;
	}
	public void setEB_NO(String EB_NO) {
		this.EB_NO = EB_NO;
	}
	public String getEB_NAME() {
		return EB_NAME;
	}
	public void setEB_NAME(String EB_NAME) {
		this.EB_NAME = EB_NAME;
	}
	public String getEB_MAIN() {
		return EB_MAIN;
	}
	public void setEB_MAIN(String EB_MAIN) {
		this.EB_MAIN = EB_MAIN;
	}
	
}
