package next.services.bindclass;

public class Bindclass {
	
	private String CD_NO;//班級代號
	private String CD_NAME;//班級名稱
	private String CD_GRADE;//班級年級
	private String CD_TEACHER;//班級導師
	private String CD_SCHOOL;//班級所屬學校
	private String CD_MEMBER;//班級成員
	
	public Bindclass() {
	}
	
	public String getCD_NO() {
		return CD_NO;
	}
	public void setCD_NO(String CD_NO) {
		this.CD_NO = CD_NO;
	}
	public String getCD_NAME() {
		return CD_NAME;
	}
	public void setCD_NAME(String CD_NAME) {
		this.CD_NAME = CD_NAME;
	}
	public String getCD_GRADE() {
		return CD_GRADE;
	}
	public void setCD_GRADE(String CD_GRADE) {
		this.CD_GRADE = CD_GRADE;
	}
	public String getCD_TEACHER() {
		return CD_TEACHER;
	}
	public void setCD_TEACHER(String CD_TEACHER) {
		this.CD_TEACHER = CD_TEACHER;
	}
	public String getCD_SCHOOL() {
		return CD_SCHOOL;
	}
	public void setCD_SCHOOL(String CD_SCHOOL) {
		this.CD_SCHOOL = CD_SCHOOL;
	}
	public String getCD_MEMBER() {
		return CD_MEMBER;
	}
	public void setCD_MEMBER(String CD_MEMBER) {
		this.CD_MEMBER = CD_MEMBER;
	}
	
}
