package next.services.focus;

public class Focus {
	private String FB_BOOK;//重點冊別
	private String FB_CHAPTER;//重點章別
	private String FB_SECTION;//重點節別
	private String FB_MAIN;//重點放置位置
	
	public Focus() {	
	}
	public String getFB_BOOK() {
		return FB_BOOK;
	}
	public void setFB_BOOK(String FB_BOOK) {
		this.FB_BOOK = FB_BOOK;
	}	
	public String getFB_CHAPTER() {
		return FB_CHAPTER;
	}
	public void setFB_CHAPTERK(String FB_CHAPTER) {
		this.FB_CHAPTER = FB_CHAPTER;
	}
	public String getFB_SECTION() {
		return FB_SECTION;
	}
	public void setFB_SECTION(String FB_SECTION) {
		this.FB_SECTION = FB_SECTION;
	}
	public String getFB_MAIN() {
		return FB_MAIN;
	}
	public void setFB_MAIN(String FB_MAIN) {
		this.FB_MAIN = FB_MAIN;
	}		
}
